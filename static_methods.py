class adity:
    @staticmethod
    def add(x,y):
        print('the sum value:',(x+y))
    @staticmethod
    def sub(x,y):
        print('the subtraction value:',(x-y))
    @staticmethod
    def avg(x,y):
        print('the average value:',(x+y)/2)
adity.add(100,200)
adity.sub(300,200)
adity.avg(100,200)
